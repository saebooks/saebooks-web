@router.get("/tax-returns/{return_id}", response_class=HTMLResponse, response_model=None)
async def tax_returns_detail(
    request: Request, return_id: UUID
) -> HTMLResponse | RedirectResponse:
    if not _require_auth(request):
        return RedirectResponse(url="/login", status_code=303)
    gate = _au_gate(request)
    if gate is not None:
        return gate

    try:
        async with api_client(request) as client:
            resp = await client.get(f"/api/v1/tax_returns/{return_id}")
            if resp.status_code == 401:
                request.session.clear()
                return RedirectResponse(url="/login", status_code=303)
            if resp.status_code == 404:
                return HTMLResponse("Tax return not found", status_code=404)
            if not resp.is_success:
                request.session["flash_error"] = f"API error: HTTP {resp.status_code}"
                return RedirectResponse(url="/tax-returns", status_code=303)
            tax_return = resp.json()

            lodgement: dict | None = None
            if tax_return.get("lodgement_record_id"):
                try:
                    lg_resp = await client.get(
                        f"/api/v1/tax_returns/{return_id}/lodgement"
                    )
                except httpx.RequestError as exc:
                    # Secondary data — the return itself loaded fine, so
                    # degrade to "no lodgement shown" rather than failing
                    # the whole detail page.
                    logger.warning(
                        "tax-returns/detail: lodgement fetch unreachable: %s", exc
                    )
                else:
                    if lg_resp.is_success:
                        lodgement = lg_resp.json()
    except httpx.RequestError as exc:
        logger.error("tax-returns/detail: API unreachable: %s", exc)
        request.session["flash_error"] = _UNREACHABLE_MESSAGE
        return RedirectResponse(url="/tax-returns", status_code=303)

    figures = tax_return.get("figures") or {}
    return_type = tax_return.get("return_type") or ""
    jurisdiction = tax_return.get("jurisdiction") or ""

    # Box-vector figures ({box_code: {amount, label, display_order}}) vs
    # TSD's list-shaped figures ({main, lisa1, errors, ...}) — see
    # persist_return / persist_tsd_return in the engine.
    is_box_vector = return_type in ("KMD", "BAS", "IAS") and all(
        isinstance(v, dict) and "amount" in v for v in figures.values()
    )
    boxes: list[dict] = []
    if is_box_vector:
        boxes = sorted(
            (
                {"code": code, **val}
                for code, val in figures.items()
                if isinstance(val, dict)
            ),
            key=lambda b: b.get("display_order", 0),
        )

    tsd_errors = figures.get("errors") if return_type == "TSD" else None
    tsd_main = figures.get("main") if return_type == "TSD" else None
    tsd_lisa1_count = len(figures.get("lisa1") or []) if return_type == "TSD" else 0

    exportable = (jurisdiction, return_type) in _EXPORTABLE_TYPES

    flash = request.session.pop("flash", None)
    flash_error = request.session.pop("flash_error", None)

    return _TEMPLATES.TemplateResponse(
        request,
        "tax_returns/detail.html",
        {
            "tax_return": tax_return,
            "lodgement": lodgement,
            "boxes": boxes,
            "is_box_vector": is_box_vector,
            "tsd_errors": tsd_errors,
            "tsd_main": tsd_main,
            "tsd_lisa1_count": tsd_lisa1_count,
            "exportable": exportable,
            "flash": flash,
            "flash_error": flash_error,
        },
    )


