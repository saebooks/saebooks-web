@router.post("/tax-returns/generate", response_class=HTMLResponse, response_model=None)
async def tax_returns_generate(request: Request) -> RedirectResponse:
    if not _require_auth(request):
        return RedirectResponse(url="/login", status_code=303)

    form = await request.form()
    return_type = str(form.get("return_type") or "").strip().upper()
    period_id = str(form.get("period_id") or "").strip()

    if not return_type or not period_id:
        request.session["flash_error"] = "Return type and period are both required."
        return RedirectResponse(url="/tax-returns", status_code=303)

    try:
        UUID(period_id)
    except ValueError:
        request.session["flash_error"] = f"'{period_id}' is not a valid period ID."
        return RedirectResponse(url="/tax-returns", status_code=303)

    payload = {
        "jurisdiction": _jurisdiction(request),
        "period_id": period_id,
        "return_type": return_type,
    }

    async with api_client(request) as client:
        resp = await client.post("/api/v1/tax_returns/generate", json=payload)

    if resp.status_code == 401:
        request.session.clear()
        return RedirectResponse(url="/login", status_code=303)

    if resp.status_code == 201:
        body = resp.json()
        request.session["flash"] = (
            f"{return_type} generated for this period — status "
            f"{body.get('status', 'ready')}."
        )
        return RedirectResponse(url=f"/tax-returns/{body['id']}", status_code=303)

    # Honest surfacing: 422 (no box definitions — e.g. TSD, or a period the
    # engine can't find) and any other failure both come from the engine
    # verbatim, never swallowed into a generic "something went wrong".
    detail = f"Generate failed: HTTP {resp.status_code}"
    try:
        body_detail = resp.json().get("detail")
        if isinstance(body_detail, str) and body_detail:
            detail = body_detail
    except Exception:
        pass
    request.session["flash_error"] = detail
    return RedirectResponse(url="/tax-returns", status_code=303)


