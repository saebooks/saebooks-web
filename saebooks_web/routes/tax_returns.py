@router.post(
    "/tax-returns/{return_id}/file", response_class=HTMLResponse, response_model=None
)
async def tax_returns_mark_filed(
    request: Request, return_id: UUID
) -> RedirectResponse:
    if not _require_auth(request):
        return RedirectResponse(url="/login", status_code=303)

    form = await request.form()
    reference = str(form.get("reference") or "").strip()
    payload: dict[str, Any] = {}
    if reference:
        payload["reference"] = reference

    async with api_client(request) as client:
        resp = await client.post(f"/api/v1/tax_returns/{return_id}/file", json=payload)

    if resp.status_code == 401:
        request.session.clear()
        return RedirectResponse(url="/login", status_code=303)
    if resp.status_code == 404:
        request.session["flash_error"] = "Tax return not found."
    elif resp.status_code == 422:
        detail = "This return has already been filed."
        try:
            body_detail = resp.json().get("detail")
            if isinstance(body_detail, str) and body_detail:
                detail = body_detail
        except Exception:
            pass
        request.session["flash_error"] = detail
    elif resp.is_success:
        body = resp.json()
        filed_at = body.get("filed_at") or ""
        request.session["flash"] = f"Marked filed{f' at {filed_at}' if filed_at else ''}."
    else:
        request.session["flash_error"] = f"Mark-filed failed: HTTP {resp.status_code}"

    return RedirectResponse(url=f"/tax-returns/{return_id}", status_code=303)
