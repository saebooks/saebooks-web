@router.get("/tax-returns", response_class=HTMLResponse, response_model=None)
async def tax_returns_list(
    request: Request,
    return_type: str | None = None,
    status: str | None = None,
) -> HTMLResponse | RedirectResponse:
    if not _require_auth(request):
        return RedirectResponse(url="/login", status_code=303)
    gate = _au_gate(request)
    if gate is not None:
        return gate

    return_type = (return_type or "").strip().upper() or None
    status_filter = (status or "").strip().lower() or None
    if status_filter not in (None, *_STATUSES):
        status_filter = None

    params: dict[str, Any] = {}
    if return_type:
        params["return_type"] = return_type
    if status_filter:
        params["status"] = status_filter

    returns: list[dict] = []
    error: str | None = None

    try:
        async with api_client(request) as client:
            resp = await client.get("/api/v1/tax_returns", params=params)
    except httpx.RequestError as exc:
        logger.error("tax-returns/list: API unreachable: %s", exc)
        error = _UNREACHABLE_MESSAGE
    else:
        if resp.status_code == 401:
            request.session.clear()
            return RedirectResponse(url="/login", status_code=303)
        if resp.is_success:
            returns = resp.json().get("items", [])
        else:
            error = f"API error: HTTP {resp.status_code}"

    # Best-effort suggestions for the Generate form's period field — every
    # distinct period_id already seen on a return for this company. Not a
    # real picker (see module docstring's engine-gap note), just a memory
    # aid for the common re-generate-for-the-same-period case.
    known_period_ids = sorted({r["period_id"] for r in returns if r.get("period_id")})

    flash = request.session.pop("flash", None)
    flash_error = request.session.pop("flash_error", None)
    pending_duplicate_generate = request.session.pop("pending_duplicate_generate", None)

    return _TEMPLATES.TemplateResponse(
        request,
        "tax_returns/list.html",
        {
            "returns": returns,
            "total": len(returns),
            "error": error,
            "flash": flash,
            "flash_error": flash_error,
            "pending_duplicate_generate": pending_duplicate_generate,
            "filter_return_type": return_type or "",
            "filter_status": status_filter or "",
            "return_types": _RETURN_TYPES,
            "generatable_types": _GENERATABLE_TYPES,
            "statuses": _STATUSES,
            "known_period_ids": known_period_ids,
            "jurisdiction": _jurisdiction(request),
        },
    )


