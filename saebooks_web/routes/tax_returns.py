"""Statutory returns (Deklaratsioonid / Tax returns) — EE KMD/TSD surface.

Thin proxy over the engine's ``/api/v1/tax_returns`` surface (see the
engine worktree's ``saebooks/api/v1/tax_returns.py`` — Packet 4c,
commit 734b901, plus the pre-existing CRUD/list/lodge routes it extends).
The AU equivalent of this screen is the existing BAS worksheet
(``/reports/bas-summary``) + ATO SBR wizard (``/admin/ato-sbr``) — both
untouched by this packet; the sidebar surfaces this page only for a
non-AU (currently: EE) active company (see ``templates/base.html``'s
``jurisdiction`` gating, Packet 1).

GET  /tax-returns                    — list (filter by return_type/status)
POST /tax-returns/generate           — POST /api/v1/tax_returns/generate
GET  /tax-returns/{id}               — detail (figures, lodgement, actions)
GET  /tax-returns/{id}/export        — relay GET .../export (XML download)
POST /tax-returns/{id}/file          — POST .../file (manual file-and-confirm)

Known engine gap (do not fabricate a fix here — this is a proxy, the
engine repo is out of scope for this packet): there is no
``GET /api/v1/tax_periods`` (list) or create endpoint. ``TaxPeriod`` rows
are provisioned entirely outside this API surface today (payroll TSD
generation and test fixtures insert them directly). The "Generate"
action therefore cannot offer a real period picker — it takes a raw
period UUID, pre-filled from any period already visible in the list
(a `<datalist>` of period ids seen on returns already on file), so at
least the common "generate a second return type for a period we already
have a return for" case doesn't require copy-pasting from elsewhere.
Flagged, not hidden.

Return-type surface (see engine router docstring):
  * ``/generate`` only computes box-vector types (AU BAS/IAS, EE KMD) —
    422 for anything else (e.g. TSD, a list-shaped annex type).
  * ``/export`` only renders AU BAS/IAS and EE KMD — 501 for anything else
    (e.g. TSD — its own CSV/XML builders exist service-side but aren't
    wired to a generic HTTP surface, per the engine router's own
    docstring). The download button is only offered for exportable types;
    TSD returns (which land here via the payroll flow, not this screen's
    Generate button) show their figures inline instead, including any
    ``figures.errors`` data-quality warnings — surfaced, never swallowed.

Status vocabulary: the engine's ``tax_returns.status`` column is
lowercase text (``draft``/``ready``/``lodged``/``accepted``/``rejected``/
``amended``/``filed`` — see ``models/tax_return.py``'s ``TaxReturnStatus``
StrEnum), NOT the UPPERCASE convention other saebooks-web domains use
(invoices/bills/inbox). Compared against lowercase; only capitalised for
display.
"""
from __future__ import annotations

import logging
from pathlib import Path
from typing import Any
from uuid import UUID

import httpx
from fastapi import APIRouter, Request
from fastapi.responses import HTMLResponse, RedirectResponse, Response
from fastapi.templating import Jinja2Templates

from saebooks_web.api_client import api_client

logger = logging.getLogger("saebooks_web.tax_returns")

router = APIRouter()

_UNREACHABLE_MESSAGE = "Couldn't reach the tax service. Try again in a moment."

_TEMPLATES_DIR = Path(__file__).resolve().parent.parent.parent / "templates"
_TEMPLATES = Jinja2Templates(directory=str(_TEMPLATES_DIR))

# Engine's real (lowercase) status vocabulary — models/tax_return.py.
_STATUSES = ("draft", "ready", "lodged", "accepted", "rejected", "amended", "filed")
# /generate only supports box-vector return types (engine router docstring).
_GENERATABLE_TYPES = ("KMD",)
# List filter offers both EE annex types the packet names; TSD returns are
# generated by the payroll flow, not this screen, but still list/view here.
_RETURN_TYPES = ("KMD", "TSD")
# /export dispatches on (jurisdiction, return_type) — anything else 501s.
_EXPORTABLE_TYPES = {("AU", "BAS"), ("AU", "IAS"), ("EE", "KMD")}


def _require_auth(request: Request) -> str | None:
    return request.session.get("api_token")


def _jurisdiction(request: Request) -> str:
    # "AU" (not "EE") — matches the fallback used everywhere else this
    # packet reads active_company_jurisdiction (company_context.py,
    # templates/base.html, invoice_pdf_ee.py). If the middleware's probe
    # ever fails to resolve for a company that reached this screen, the
    # payload we send the engine must default the same safe direction as
    # every other consumer, not diverge to "EE".
    return getattr(request.state, "active_company_jurisdiction", None) or "AU"


def _au_gate(request: Request) -> HTMLResponse | None:
    """This screen is EE-only (see module docstring) — templates/base.html
    hides its nav link for an AU company, but the route itself had no
    matching server-side check, so a bookmark/autocomplete/shared link/
    crawler could reach it directly for a company we know is AU. Only an
    explicitly-resolved "AU" is blocked; an unresolved (None) jurisdiction
    fails open, same posture as the rest of this packet (see _jurisdiction
    above and company_context.py's "ambiguous" branch).

    Critic round 3 raised this as a divergence from _jurisdiction()'s
    None -> "AU" default and asked to flip it closed. Deliberately NOT
    changed: reviewed and kept as a documented tradeoff, not a defect —
    see the round-3 fixer's report. Flipping this would lock out a
    legitimate EE user (this screen's actual audience) on every transient
    jurisdiction-probe blip, in exchange for hiding a cosmetic wrong-screen
    view from an AU company in the same rare window — and _jurisdiction()
    already keeps the data this screen writes (the generate payload)
    AU-safe regardless of this gate's posture. Symmetric tradeoff, not
    worth relitigating without new evidence of actual harm."""
    if getattr(request.state, "active_company_jurisdiction", None) == "AU":
        return HTMLResponse("Not found", status_code=404)
    return None


