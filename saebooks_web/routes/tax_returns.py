@router.get("/tax-returns/{return_id}/export", response_model=None)
async def tax_returns_export(
    request: Request, return_id: UUID
) -> Response | RedirectResponse:
    if not _require_auth(request):
        return RedirectResponse(url="/login", status_code=303)
    gate = _au_gate(request)
    if gate is not None:
        return gate

    try:
        async with api_client(request) as client:
            resp = await client.get(f"/api/v1/tax_returns/{return_id}/export")
    except httpx.RequestError as exc:
        logger.error("tax-returns/export: API unreachable: %s", exc)
        request.session["flash_error"] = _UNREACHABLE_MESSAGE
        return RedirectResponse(url=f"/tax-returns/{return_id}", status_code=303)

    if resp.status_code == 401:
        request.session.clear()
        return RedirectResponse(url="/login", status_code=303)
    if resp.status_code == 404:
        request.session["flash_error"] = "Tax return not found."
        return RedirectResponse(url="/tax-returns", status_code=303)
    if not resp.is_success:
        # 501 (no document builder — e.g. TSD) / 422 (missing period or
        # company ABN/regcode) both surface verbatim rather than a raw
        # error page on what looks like a download link.
        detail = f"Export failed: HTTP {resp.status_code}"
        try:
            body_detail = resp.json().get("detail")
            if isinstance(body_detail, str) and body_detail:
                detail = body_detail
        except Exception:
            pass
        request.session["flash_error"] = detail
        return RedirectResponse(url=f"/tax-returns/{return_id}", status_code=303)

    content_disposition = resp.headers.get(
        "content-disposition", f'attachment; filename="tax_return_{return_id}.xml"'
    )
    return Response(
        content=resp.content,
        media_type=resp.headers.get("content-type", "application/xml"),
        headers={"Content-Disposition": content_disposition},
    )


